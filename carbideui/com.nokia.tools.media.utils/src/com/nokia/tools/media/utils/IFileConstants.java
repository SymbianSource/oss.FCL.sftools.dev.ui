package com.nokia.tools.media.utils;

/**
 * Constants for common file types.
 * 
 */
public interface IFileConstants {
	/**
	 * Extension for svg.
	 */
	String FILE_EXT_SVG = "svg";
	/**
	 * Extension for svg with dot prefix.
	 */
	String FILE_EXT_DOTSVG = "." + FILE_EXT_SVG;
	/**
	 * Extension for svg tiny.
	 */
	String FILE_EXT_SVG_TINY = "svgt";
	/**
	 * Extension for svg tiny with dot prefix.
	 */
	String FILE_EXT_DOTSVG_TINY = "." + FILE_EXT_SVG_TINY;
	/**
	 * Extension for bitmap.
	 */
	String FILE_EXT_BMP = "bmp";
	/**
	 * Extension for bitmap with dot.
	 */
	String FILE_EXT_DOTBMP = "." + FILE_EXT_BMP;
	
	/**
	 * Extension for S60 theme tdf file
	 */
	String FILE_EXT_TDF = ".tdf"  ;
	
	
}
